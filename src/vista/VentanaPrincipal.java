    private void btnIniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIniciarActionPerformed
        // TODO add your handling code here:

        int fila = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese la cantidad de filas"));

        int columna = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese las columnas"));
        
        if (fila <= 6 && columna <= 10) {

            botones = new JButton[fila][columna];
            controlador = new Controlador(fila, columna);
            int numBomba = 1;
            Bomba bomba = new Bomba(numBomba);
            
            int min = 1;
            int max = controlador.getCasillas().length;
            
            int filaB = (int) (Math.random() * (max - min + 1));
            int columnaB = (int) (Math.random() * (max - min + 1));
            controlador.agregarBomba(bomba, filaB, columnaB);
            

            cargarBotones();

            btnIniciar.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(null, "Lo sentimos, solo se permite un maximo de 6 filas\n"
                    + "y 10 columnas");
        }
    }//GEN-LAST:event_btnIniciarActionPerformed

