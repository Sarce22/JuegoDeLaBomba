    public void cargarBotones() {
        int ancho = 70;
        int alto = 90;
        int separado = 20;
        String texto = "?";

        for (int i = 0; i < botones.length; i++) {
            for (int j = 0; j < botones[i].length; j++) {

                botones[i][j] = new JButton();
                // setBounds(posX, posY, ancho, alto)
                botones[i][j].setBounds(
                        (j * ancho + separado),
                        (i * alto + separado),
                        ancho, alto);

                botones[i][j].setText(texto);
                botones[i][j].setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/signoX64.png.png")));
                botones[i][j].setHorizontalTextPosition(SwingConstants.CENTER);
                botones[i][j].setVerticalTextPosition(SwingConstants.CENTER);
                botones[i][j].addActionListener(this);
                panel.add(botones[i][j]);

            }

        }
        panel.repaint();

    }

    @Override
    public void actionPerformed(ActionEvent ae) {
        for (int i = 0; i < botones.length; i++) {
            for (int j = 0; j < botones[i].length; j++) {
                if (ae.getSource().equals(botones[i][j])) {
                    Casilla casilla = controlador.obtenerCasillar(i, j);
                    if (casilla.getEstado().equals(Casilla.DESCONOCIDO)) {
                        JOptionPane.showMessageDialog(this, "No hay bomba, Continua");
                    }
                    if (casilla.getEstado().equals(Casilla.BOMBA)) {
                        VentanaBoom ventana = new VentanaBoom(this, i, j);
                        ventana.setVisible(true);
                        this.dispose();
                    } else {
                        botones[i][j].setVisible(false);
                    }
                }

            }

        }
    }

    public void validarPosiciones() {
        for (int i = 0; i < botones.length; i++) {
            for (int j = 0; j < botones[i].length; j++) {
                Casilla casilla = controlador.obtenerCasillar(i, j);
                if (casilla.getEstado().equals(Casilla.DESCONOCIDO)) {
                    botones[i][j].setBackground(Color.WHITE);
//                    botones[i][j].setVisible(false);

                } else {
                    botones[i][j].setBackground(Color.RED);
//                  botones[i][j].setVisible(false);
                    botones[i][j].setText(String.valueOf(casilla.getBomba().getNumBomba()));
                }

            }

        }
    }

